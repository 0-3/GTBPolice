package com.sucy.police.config;

/**
 * Configuration nodes for laws
 */
public class LawNodes {

    public static final String

        /**
         * Latest laws added
         */
        LATEST = "latest",

        /**
         * Database of registered laws
         */
        LAWS = "laws",

        /**
         * Number of laws removed
         */
        REMOVED = "removed",

        /**
         * Titles of categories
         */
        TITLES = "titles",

        /**
         * URLs of categories
         */
        URLS = "urls",

        /**
         * Names of laws
         */
        NAMES = "law-names";

}
